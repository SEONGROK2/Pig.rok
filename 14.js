// falsy한 값, truthy한 값

// if (true 또는 false) {
//     // main logic
// }


// falsy한 값1
if (0) {
    // main logic
    console.log("Hello");
}

// falsy한 값2
if ("") {
    // main logic
    console.log("Hello");
}

// falsy한 값3
if (null) {
    // main logic
    console.log("Hello");
}

// falsy한 값4
if (undefined) {
    // main logic
    console.log("Hello");
}

// falsy한 값5
if (NaN) {
    // main logic
    console.log("Hello");
}

// falsy한 값6
if (false) {
    // main logic
    console.log("Hello");
}

// truthy한 값
if (true) {
    // main logic
    console.log("Hello");
}
